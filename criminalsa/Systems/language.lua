function loc(player,text)
    if(not player)then
		player=localPlayer;
	end

    if(player and isElement(player)and getElementType(player)=="player")then
        local Language=tonumber(getElementData(player,"Language"))or 0;
        if(Translation[Language])then
            return Translation[Language][text] or "N/A";
        else
            return "N/A";
        end
    end
end